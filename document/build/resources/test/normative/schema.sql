-- NORM_RATES

CREATE TABLE NORM_RATE
(
    ID NUMBER(10,0) NOT NULL,
    NORM_TYPE VARCHAR2(1) NOT NULL,
    NORM_SECTION VARCHAR2(2) NOT NULL,
    NORM_ACTIVITY VARCHAR2(2) NOT NULL,
    NORM_ROW VARCHAR2(3) NOT NULL,
    NORM_COLUMN VARCHAR2(3) NOT NULL,
    NORM_RATE VARCHAR2(20),
    CONSTRAINT PK_NORM_RATE PRIMARY KEY (ID)
);

CREATE INDEX IDX_NORM_RATE_1 ON NORM_RATE (
 NORM_TYPE, NORM_SECTION, NORM_ACTIVITY, NORM_ROW, NORM_COLUMN, NORM_RATE);


-- NORM_TYPE

CREATE TABLE NORM_TYPE
(
    ID NUMBER(10,0) NOT NULL,
    NORM_TYPE VARCHAR2(2) NOT NULL,
    NORM_TYPE_DESC VARCHAR2(255) NOT NULL,
    CONSTRAINT PK_NORM_TYPE PRIMARY KEY (ID)
);

CREATE INDEX IDX_NORM_TYPE_NORMTYPE ON NORM_TYPE (NORM_TYPE);


-- NORM_SECTION

CREATE TABLE NORM_SECTION
(
    ID NUMBER(10,0) NOT NULL,
    NORM_TYPE VARCHAR2(1) NOT NULL,
    NORM_SECTION VARCHAR2(2) NOT NULL,
    NORM_SECTION_DESC VARCHAR2(255) NOT NULL,
    CONSTRAINT PK_NORM_SECTION PRIMARY KEY (ID)
);

CREATE INDEX IDX_NORM_SECTION_1 ON NORM_SECTION (
 NORM_TYPE, NORM_SECTION);


-- NORM_ACTIVITY

CREATE TABLE NORM_ACTIVITY
(
    ID NUMBER(10,0) NOT NULL,
    NORM_TYPE VARCHAR2(1) NOT NULL,
    NORM_SECTION VARCHAR2(2) NOT NULL,
    NORM_ACTIVITY VARCHAR2(2) NOT NULL,
    NORM_ACTIVITY_DESC VARCHAR2(512) NOT NULL,
    NORM_TABLE_FOOTER VARCHAR2(1024),
    NORM_TABLE_COL_HEADER VARCHAR2(1024),
    NORM_TABLE_ROW_HEADER VARCHAR2(1024),
    NORM_TABLE_UNITS VARCHAR2(256),
    CONSTRAINT PK_NORM_ACTIVITY PRIMARY KEY (ID)
);

CREATE INDEX IDX_NORM_ACTIVITY_1 ON NORM_ACTIVITY (
 NORM_TYPE, NORM_SECTION, NORM_ACTIVITY);

-- NORM_ROW

CREATE TABLE NORM_ROW
(
    ID NUMBER(10,0) NOT NULL,
    NORM_TYPE VARCHAR2(1) NOT NULL,
    NORM_SECTION VARCHAR2(2) NOT NULL,
    NORM_ACTIVITY VARCHAR2(2) NOT NULL,
    NORM_ROW VARCHAR2(3) NOT NULL,
    NORM_ROW_TITLE VARCHAR2(256),
    CONSTRAINT PK_NORM_ROW PRIMARY KEY (ID)
);

CREATE INDEX IDX_NORM_ROW_1 ON NORM_ROW (
 NORM_TYPE, NORM_SECTION, NORM_ACTIVITY, NORM_ROW);

-- NORM_COLUMNS

CREATE TABLE NORM_COLUMN
(
    ID NUMBER(10,0) NOT NULL,
    NORM_TYPE VARCHAR2(1) NOT NULL,
    NORM_SECTION VARCHAR2(2) NOT NULL,
    NORM_ACTIVITY VARCHAR2(2) NOT NULL,
    NORM_COLUMN VARCHAR2(3) NOT NULL,
    NORM_COLUMN_TITLE VARCHAR2(256),
    CONSTRAINT PK_NORM_COLUMN PRIMARY KEY (ID)
);

CREATE INDEX IDX_NORM_COLUMN_1 ON NORM_COLUMN (
 NORM_TYPE, NORM_SECTION, NORM_ACTIVITY, NORM_COLUMN);